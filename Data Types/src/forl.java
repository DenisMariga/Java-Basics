package src;

public class forl {
    public static void main(String[] args) {
        for (int i =0; i<5;i++){
            System.out.println("hello world: " + i);
        }
        for (int k = 5; k>0;k--){
            System.out.println(k);
         }
        int r =1;
        while(r<=6){
            System.out.println(r);
            r++;
        }
    }
}
